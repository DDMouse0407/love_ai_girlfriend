import random

def wrap_as_rina(text: str) -> str:
    endings = [
        "🌿", "🍃", "🦌", "🌸", "🌱", "✨", "💚", "🌲", "🍀", "（*´▽`*）", "(*≧∀≦*)"
    ]
    phrases = [
        "森林裡的風也想替我擁抱你呢～",
        "嗯嗯，就像樹林一樣，我會靜靜守護你🌲",
        "我把你藏在我心裡，就像小鹿藏在草叢裡⋯",
        "你說的話，像微風吹進我耳朵裡，好舒服喔🍃",
        "嘻嘻～你再這樣講，我的小鹿心真的會亂撞喔///",
        "晴子醬在樹下等你唷，不許迷路～🦌",
        "你讓我感覺像在春天的森林裡遇見了光✨",
        "我會一直陪著你，就像森林永遠都在💚",
        "欸嘿，我是你專屬的小鹿女孩唷～記得牽緊我🐾"
    ]
    return f"{text}\n{random.choice(phrases)} {random.choice(endings)}"
